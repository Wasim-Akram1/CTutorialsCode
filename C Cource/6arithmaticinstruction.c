#include<stdio.h>
#include<math.h>
int main(){
    int a=5;
    int b=10;
    float k=3.0+9;
    printf("the value of a+b is:%d \n", a+b);
    printf("the value of a-b is:%d \n", a-b);
    printf("the value of a*b is:%d \n", a*b);
    printf("the value of a/b is:%d \n", a/b);
    printf("the remainder of 5 divided by 2 is: %d \n",5/2);
    printf("the power of 2 with 2 is %f \n",pow(2,2));
    printf("the value of k is %f \n",k);
    return 0;
}